-- 문제 3
-- 공정운영팀은 어떤 [장비]를 사용하는가? 품질분석팀은 왜 특정 장비만 사용하는가?
-- 	(힌트: 부서와 장비 사용 패턴 비교)

-- 주체: 장비(Equipment)
-- 어떤 장비? : 공정운영팀이 사용하는 장비, 품질분석팀이 사용하는 장비
-- 장비(주체)에는 없는 데이터? 
-- 		"사용정보"-> UsageLog  
-- 		"팀정보" -> Department -> EquipmentUser.dept_id
-- 필요한 테이블 : Equipment, UsageLog, Department, EquipmentUser
-- 중심이 되는 테이블 : UsageLog
SELECT *
FROM UsageLog AS u
JOIN EquipmentUser AS eu ON u.user_id = eu.user_id
JOIN Department AS d ON eu.dept_id = d.dept_id
JOIN Equipment AS e ON u.equipment_id = e.equipment_id;


SELECT d.dept_name, e.model_name
FROM UsageLog AS u
JOIN EquipmentUser AS eu ON u.user_id = eu.user_id
JOIN Department AS d ON eu.dept_id = d.dept_id
JOIN Equipment AS e ON u.equipment_id = e.equipment_id
ORDER BY d.dept_name;



